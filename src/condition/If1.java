package condition;

public class If1 {
    public static void main(String[] args) {
        int age = 20; // 사용자의 나의

        // if 문
        if (age >= 18) {
            System.out.println("성인입니다.");
        }

        if (age < 18) {
            System.out.println("미성년자 입니다.");
        }

        // else 문
        // if 문이 맞지 않다면 else문이 실행된다.
    }
}
