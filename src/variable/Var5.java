package variable;

public class Var5 {
    public static void main(String[] args) {
        // 초기화 선언을 따로 하는 방법
        int a; // 선언
        a = 1; // 초기화
        System.out.println(a);

        // 초기화 선언을 한번에 하는 방법
        int b = 2;
        System.out.println(b);

        // 초기화와 선언을 한번에 여러개 하는 방법
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
