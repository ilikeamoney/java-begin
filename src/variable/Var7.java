package variable;

public class Var7 {
    public static void main(String[] args) {
        // 자바에 다양한 자료형

        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c =  true; // 불리언(boolean) true, false 입력 가능 1, 0
        char d = 'a'; // 문자 하나만 사용
        String e = "hello java"; // 문자열, 문자열을 다루기 위한 특별한 타입

//        int z = "100"; 자료형에 맞지않는 데이터는 사용 불가
        // 개발자가 직접 선언한 데이터를 리터럴(literal) 이라고 한다 고정값

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
